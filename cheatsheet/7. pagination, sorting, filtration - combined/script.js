function sortList(sortedField, isAscending) {
	var startDate = new Date().getMilliseconds(),
		endDate;

	var tmpObject,
		wasSorted;

	do {
		wasSorted = false;

		for (var i = 0; i < globalUsers.length - 1; i++) {
			if (
				(
					isAscending &&
					globalUsers[i][sortedField] > globalUsers[i + 1][sortedField]
				) ||
				(
					!isAscending &&
					globalUsers[i][sortedField] < globalUsers[i + 1][sortedField]
				)
			) {
				wasSorted = true;

				tmpObject = globalUsers[i + 1];
				globalUsers[i + 1] = globalUsers[i];
				globalUsers[i] = tmpObject;
			}
		}
	} while (wasSorted);

	endDate = new Date().getMilliseconds();
	alert('Time: ' + (endDate - startDate));
}

function goToPage() {
	var startDate = performance.now(),
		endDate;

	var recordsOffset = (pageNumber - 1) * limit;
	var recordsNumber = recordsOffset + limit;

	clearTable();

	for (var i = recordsOffset; i < recordsNumber && i < displayedUsers.length; i++) {
		var thisUser = displayedUsers[i];

		$userList.append(
			'<tr>' +
			'<td>' +
			thisUser.id +
			'</td>' +
			'<td>' +
			thisUser.name +
			'</td>' +
			'<td>' +
			thisUser.surname +
			'</td>' +
			'</tr>'
		);
	}

	if (pageNumber === 1) {
		$pagination.find('.prev').hide();
	}
	else {
		$pagination.find('.prev').show();
	}

	if (pageNumber === totalPages) {
		$pagination.find('.next').hide();
	}
	else {
		$pagination.find('.next').show();
	}

	$pagination.find('.page-number').each(function () {
		if (Number($(this).text()) === pageNumber) {
			$(this).addClass('current-page');
		}
		else {
			$(this).removeClass('current-page');
		}
	});

	endDate = performance.now();
	alert('Go to page time: ' + (endDate - startDate));
}

function populatePagination() {
	$pagination.text('');

	$pagination.append('<a href="#" class="prev"><</a> ');

	for (var i = 1; i <= totalPages; i++) {
		$pagination.append('<a href="#" class="page-number">' + i + '</a>');
		if (i !== totalPages) {
			$pagination.append(' - ');
		}
	}

	$pagination.append(' <a href="#" class="next">></a>');
}

$filterAccept.on('click', function (e) {
	e.preventDefault();

	filterTable();
});

$filterField.on('change', setAvailableFilters);

$sortSelect.on('change', function (e) {
	var $thisSelect = $(this),
		selectValue = $thisSelect.val(),
		selectDirection = $thisSelect.find('option:checked').data('direction'),
		isAscending = 'ascending' === selectDirection;

	sortList(selectValue, isAscending);
	filterTable();
});

$tableHeaders.on('click', function (e) {
	var $thisHeader = $(this),
		headerValue = $thisHeader.data('value'),
		isAscending = $thisHeader.data('direction');

	$thisHeader.removeClass('sorted-ascending sorted-descending');

	if ('undefined' === typeof isAscending || !isAscending) {
		isAscending = true;
		$thisHeader.addClass('sorted-ascending');
	}
	else {
		isAscending = false;
		$thisHeader.addClass('sorted-descending');
	}

	$thisHeader.data('direction', isAscending);

	$('.last-sorted').removeClass('last-sorted');
	$thisHeader.addClass('last-sorted');

	sortList(headerValue, isAscending);
	filterTable();
});

$pagination.on('click', '.page-number', function (e) {
	e.preventDefault();

	if (!$(this).hasClass('current-page')) {
		pageNumber = Number($(this).text());

		goToPage();
	}
});

$pagination.on('click', '.prev', function (e) {
	e.preventDefault();

	if (pageNumber !== 1) {
		pageNumber--;
	}

	goToPage();
});

$pagination.on('click', '.next', function (e) {
	e.preventDefault();

	if (pageNumber !== totalPages) {
		pageNumber++;
	}

	goToPage();
});

setAvailableFilters();
populatePagination();
goToPage();
